    public static class MidiNotes
    {
        public static string Name(this MidiNote note) => $"{note.NoteName()} {(byte)note / 12}";

        public static string Label(this MidiNote note) => $"{note.NoteName()} {(byte)note / 12} ({(int)note} | 0x{(byte)note:X2})";

        public static string NoteName(this MidiNote note) => ((byte)note % 12) switch
        {
            0 => "C ",
            1 => "C#",
            2 => "D ",
            3 => "D#",
            4 => "E ",
            5 => "F ",
            6 => "F#",
            7 => "G ",
            8 => "G#",
            9 => "A ",
            10 => "A#",
            11 => "B ",
            _ => "UNKNOWN"
        };
    }
